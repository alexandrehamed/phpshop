            <p class="arial">L'attraction utilise une technologie d'ascenseur spécialement développée pour Disney par Otis (N°1 mondial de l'ascenseur), la filiale de United Technologies aussi responsable des aquariums géants de The Living Seas à EPCOT. La version de Floride permet de déplacer la cabine dans et à l'extérieur de sa cage (en vitesse réduite). Ce système ne fut pas utilisé dans les versions suivantes, pour des raisons techniques. C'est cette partie de l'attraction qui pose le plus de problèmes nécessitant l'arrêt de l'attraction. Les ordinateurs surveillant les ascenseurs perdent fréquemment la trace des cabines durant cette section horizontale. Les autres versions proposent à la place un système de miroir transformant les reflets en fantômes.
              Lors des premiers contacts avec Otis, les imagineers de Disney essayèrent les derniers modèles d'ascenseurs de la marque ; mais un problème existait : on ne ressentait ni la descente ni l'ascension, ce qui n'aura pas fait une bonne attraction.
              Bien que l'attraction soit présentée comme une chute libre, l'ascenseur descend de manière accélérée par rapport à la gravité. Cela donne une sensation d'apesanteur qui peut être observée si un objet est perdu durant la chute (il est déconseillé de le faire en raison des risques de blesser autrui). Cet effet est aussi utilisé par la NASA dans certains de ses simulateurs.          </div>
