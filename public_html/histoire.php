              <p class="arial">L'attraction est basée à l'origine sur l'hôtel fictif Hollywood Tower Hotel situé à proximité des collines au nord d'Hollywood. L'hôtel aurait ouvert ses portes en 1917 (ou 1929 selon l'autre version de l'histoire). D'après la légende entourant le bâtiment, le 31 octobre 1939, la nuit d'Halloween, l'hôtel accueillait une fête dans le Tip Top Club du dernier étage (le treizième). Mais le bâtiment fut frappé par la foudre et un ascenseur rempli de cinq passagers fut transporté dans la Quatrième Dimension tandis qu'une grande partie de la façade disparut de la réalité.
    Pour la version de Tokyo DisneySea l'histoire est totalement différente mais la chute est identique à la version de Californie : voir ci-dessous.
    Avec une hauteur de 66,3 m (ou 199 pieds), c'est la plus haute attraction de Walt Disney World Resort et du Disneyland Resort. Selon des membres de l'équipe chargée de la construction en Floride, cette hauteur est due à une réglementation aérienne : à partir de 200 pieds une lumière clignotante rouge doit être placée au-dessus du bâtiment. Malheureusement cela aurait gêné le thème des années 1930 de l'attraction.
    Dans un (important) effort de vraisemblance par rapport à la série télévisée, l'équipe d'imagineers de Disney visionna l'ensemble des 156 épisodes au moins deux fois. L'attraction n'est, il faut le rappeler, basée sur aucun épisode particulier. Mais de nombreux détails y font référence et plus particulièrement dans la bibliothèque de l'hôtel.
    Le rôle des employés de l'attraction est celui de groom. L'histoire veut que les descendants des employés du Hollywood Tower Hotel rouvrent l'hôtel afin de permettre au public de connaître l'histoire de ce haut lieu qui accueillait auparavant l'élite du show-business. Bien que n'étant ni méchant ni sinistre (contrairement aux domestiques du Phantom Manor au Parc Disneyland), les grooms du Hollywood Tower Hotel sont très serviables. Mais au fur et à mesure que l'on s'approche des portes des ascenseurs de service, ils deviennent moins souriant et étranges, pratiquant l'humour noir à foison. La popularité des grooms est telle que des sites internet et des forums leurs sont consacrés. Le rôle des employés de la Tour de la Terreur de Tokyo DisneySea est totalement différent. Moins effrayant, ils sont les employés de Monsieur Hightower.</p>
