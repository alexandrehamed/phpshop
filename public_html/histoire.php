              <p class="arial">Les visiteurs qui entrent dans l'attraction sont amenés dans la bibliothèque pour visionner le petit film présenté par Rod Serling à travers une vieille télévision. Ce court métrage nous informe sur les événements qui frappèrent l'hôtel. Ensuite, les visiteurs empruntent un couloir de maintenance menant à la chaufferie. Là, un ascenseur de service aménagé peut accueillir 21 personnes sur six rangées de fauteuils permet de monter dans les étages.
Voici la première différence qui apparaît peu de temps après la fermeture des portes de l'ascenseur : l'ascenseur s'élève quelques secondes avant de s'arrêter un moment, pendant que la voix de Rod Serling annonce aux participants : « You are the passengers on a most uncommon elevator about to ascend into your very own episode of The Twilight Zone.» (« Vous êtes les passagers d'un des ascenseurs les plus stupéfiants qui soient, et vous êtes sur le point de monter pour votre propre épisode de la quatrième dimension. ») Les portes s'ouvrent pour révéler un couloir dans lequel apparaissent les cinq personnes disparus en 1939 sous des formes fantomatiques, qui disparaissent ensuite comme pour nous inviter à entrer (un rappel de la scène du couloir de Phantom Manor). Le couloir se dissipe dans un ciel de nuit étoilée, et une fenêtre se brise. L'ascenseur se referme et commence à remonter. Le narrateur continue : « One stormy night long ago, five people stepped through the door of an elevator and into a nightmare. That door is opening once again, and this time, it's opening for you. » (« Par une nuit d'orage il y a fort longtemps, cinq personnes passèrent les portes d'un ascenseur pour vivre un cauchemar. L'orage est maintenant de retour et les portes s'entrouvrent à nouveau, et cette fois-ci, elles s'ouvrent pour vous. »)
Ensuite un peu plus haut, les portes s'ouvrent à nouveau et la cabine se déplace mystérieusement en dehors de la cage, à travers une section de l'attraction appelée la Quatrième Dimension : une collection surréaliste d'objets et de signes venus d'on ne sait où, dans le style de la séquence d'ouverture de la série.
La cabine s'arrête dans le noir complet. La voix de Rod Serling résonne soudain de nulle part : « You are about to discover what lies beyond the fourth dimension, beyond the deepest, darkest corner of the imagination... in the tower of terror! » (« Vous êtes sur le point de découvrir ce qui repose au-delà de la quatrième dimension, au-delà des plus profonds, des plus obscurs recoins de l'imagination... ...dans la tour de la terreur ! »)
La séquence de chute commence. À l'un des moments de la chute, les portes de l'ascenseur s'ouvrent pour révéler une vue du parc à une hauteur d'une dizaine d'étages. Pour être plus précis, ce sont les deux étages juste sous le nom de l'hôtel sur la façade.
Quelques années après l'ouverture de la version de Floride, des séquences différentes de chute et de remontées ont été conçues et sont choisies de manière aléatoire afin de rendre chaque voyage dans l'attraction une expérience unique.
Après une série de ces chutes, l'ascenseur s'arrête en bas de la cage et effectue un quart de tour. La voix de Rod Serling se fait entendre une dernière fois : « A warm welcome back to those of you who made it, and a friendly word of warning; something you won't find in any guidebook. The next time you check into a deserted hotel on the dark side of Hollywood, make sure you know just what kind of vacancy you're filling. Or you may find yourself a permanent resident of... the Twilight Zone. » ( « Un accueil chaleureux à ceux d'entre vous qui l'ont fait, et un conseil d'ami ; quelque chose que vous ne trouverez dans aucun guide. La prochaine fois que vous réserverez dans un hôtel abandonné de la partie sombre d'Hollywood, prenez bien soin de préciser combien de temps vous restez. Ou vous risquez de devenir des résidents permanents... de la quatrième dimension. ») À la sortie, une boutique propose des souvenirs et la photo prise durant la chute.            
